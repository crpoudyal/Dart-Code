void main() {
  //Valid Variable naming
  var name = "chudarj";
  var f_name = 'cr';
  var gpa = 3.25;
  var is_rainning = false;
  var city1 = "Biratnagar";
  var $money = 30000;

  print(name + " " + f_name);
  print(gpa);
  print(is_rainning);
  print(city1);
  print($money);

  //Invalid Variable naming

  // var 1person = "xyz";
  // var #demo =1;
  // var final =2;
  //We cannot use reserved keyword as variable name

  final data = 977;
  // data = 978;  the final variable data can only be set once
  print(data);

  const keys = 45.78;
  print(keys);
}
